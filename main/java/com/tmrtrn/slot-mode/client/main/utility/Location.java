package com.tmrtrn.mrgood.client.main.utility;

public class Location {

	public double dLeft;
	public double dTop;
	public double dWidth;
	public double dHeight;
	
	public Location(double dLeft, double dTop, double dWidth, double dHeight)
	{
		this.dLeft = dLeft;
		this.dTop = dTop;
		this.dWidth = dWidth;
		this.dHeight = dHeight;
	}
}
